const DEFAULT_LANG = "id";
const STORAGE_KEY = "kurotech-language";

function getNestedValue(source, path) {
  return path.split(".").reduce((current, key) => current?.[key], source);
}

function translatePage(lang) {
  const dictionary = translations[lang] || translations[DEFAULT_LANG];

  document.documentElement.lang = lang;
  document.title = dictionary.meta.title;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getNestedValue(dictionary, element.dataset.i18n);
    if (typeof value === "string") {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    const attrMap = element.dataset.i18nAttr.split(";");
    attrMap.forEach((pair) => {
      const [attribute, key] = pair.split(":").map((item) => item.trim());
      const value = getNestedValue(dictionary, key);
      if (attribute && typeof value === "string") {
        element.setAttribute(attribute, value);
      }
    });
  });

  document.querySelectorAll(".language-btn").forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem(STORAGE_KEY, lang);
}

function setupLanguageSwitcher() {
  const savedLang = localStorage.getItem(STORAGE_KEY);
  const browserLang = navigator.language?.toLowerCase().startsWith("en") ? "en" : DEFAULT_LANG;
  const initialLang = translations[savedLang] ? savedLang : browserLang;

  translatePage(initialLang);

  document.querySelectorAll(".language-btn").forEach((button) => {
    button.addEventListener("click", () => translatePage(button.dataset.lang));
  });
}

function setupHeaderScroll() {
  const header = document.getElementById("siteHeader");
  const setState = () => header.classList.toggle("is-scrolled", window.scrollY > 8);
  setState();
  window.addEventListener("scroll", setState, { passive: true });
}

function setupMobileMenu() {
  const button = document.getElementById("menuToggle");
  const nav = document.getElementById("siteNav");

  if (!button || !nav) return;

  const setOpen = (isOpen) => {
    button.classList.toggle("is-open", isOpen);
    nav.classList.toggle("is-open", isOpen);
    button.setAttribute("aria-expanded", String(isOpen));

    const currentLang = document.documentElement.lang || DEFAULT_LANG;
    const labelKey = isOpen ? "nav.closeMenu" : "nav.openMenu";
    button.setAttribute("aria-label", getNestedValue(translations[currentLang], labelKey));
  };

  button.addEventListener("click", () => {
    setOpen(!nav.classList.contains("is-open"));
  });

  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => setOpen(false));
  });

  window.addEventListener("keydown", (event) => {
    if (event.key === "Escape") setOpen(false);
  });
}

function setupFooterYear() {
  const year = document.getElementById("year");
  if (year) year.textContent = new Date().getFullYear();
}

setupHeaderScroll();
setupMobileMenu();
setupFooterYear();
setupLanguageSwitcher();
